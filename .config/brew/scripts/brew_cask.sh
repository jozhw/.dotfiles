#!/usr/bin/env bash

# Install command-line tools using Homebrew with --cask.

# Install miniconda for python envs.
brew install --cask miniconda

# Install fastmail client
brew install --cask fmail2

# Install emacs with GUI
brew install --cask emacs

# Install macfuse to extend file handling capabilities
brew install --cask macfuse

# Install inkscape for photo editing
brew install --cask inkscape
